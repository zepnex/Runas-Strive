package edu.kit.informatik.model.abilities;

/**
 * Enum for the different types of cards.
 *
 * @author unyrg
 * @version 1.0
 */
public enum CardType {
    /**
     * Defines the type of a card as a defensive card.
     */
    DEFENSIVE,
    /**
     * Defines the type of a card as a offensive card.
     */
    OFFENSIVE,
    /**
     * Defines the type of a card as a neutral card.
     */
    NONE

}
